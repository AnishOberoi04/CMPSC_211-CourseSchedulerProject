    private void addDisplayClassesDisplayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDisplayClassesDisplayButtonActionPerformed
        // TODO add your handling code here:
        ArrayList<ClassDescription> classes = MultiTableQueries.getAllClassDescriptions(currentSemester);
        String tableStr = "Course Code\tDescription\tSeats\n";
        
        for(ClassDescription row : classes) {
            tableStr += String.format("%s\t%s\t%s\n", row.getCourseCode(), row.getDescription(), row.getSeats());
        }
        
        displayClassesTextArea.setText(tableStr);
    }//GEN-LAST:event_addDisplayClassesDisplayButtonActionPerformed

    private void addDisplayScheduleDisplayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDisplayScheduleDisplayButtonActionPerformed
        // TODO add your handling code here:
        StudentEntry student = (StudentEntry)studentScheduleComboBox.getSelectedItem();
        ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(currentSemester, student.getStudentID());
        String tableStr = "Course Code\tStatus\n";
        
        for(ScheduleEntry row : schedule) {
            String status = (row.getStatus().equals("S")) ? "Scheduled" : "Waitlisted";
            tableStr += String.format("%s\t%s\n", row.getCourseCode(), status);
        }
        
        displayStudentScheduleTextArea.setText(tableStr);
    }//GEN-LAST:event_addDisplayScheduleDisplayButtonActionPerformed

    private void checkData() {
        try {
            FileReader reader = new FileReader("xzq789yy.txt");
            BufferedReader breader = new BufferedReader(reader);

            String encodedAuthor = breader.readLine();
            String encodedProject = breader.readLine();
            byte[] decodedAuthor = Base64.getDecoder().decode(encodedAuthor);
            author = new String(decodedAuthor);
            byte[] decodedProject = Base64.getDecoder().decode(encodedProject);
            project = new String(decodedProject);
            reader.close();

        } catch (FileNotFoundException e) {
            //get user info and create file
            author = JOptionPane.showInputDialog("Enter your first and last name.");
            project = "Course Scheduler Fall 2023";

            //write data to the data file.
            try {
                FileWriter writer = new FileWriter("xzq789yy.txt", true);
                BufferedWriter bufferedWriter = new BufferedWriter(writer);

                // encode the output data.
                String encodedAuthor = Base64.getEncoder().encodeToString(author.getBytes());

                bufferedWriter.write(encodedAuthor);
                bufferedWriter.newLine();

                String encodedProject = Base64.getEncoder().encodeToString(project.getBytes());
                bufferedWriter.write(encodedProject);

                bufferedWriter.close();
            } catch (IOException ioe) {
                ioe.printStackTrace();
                System.exit(1);
            }

        } catch (IOException e) {
            e.printStackTrace();
            System.exit(1);
        }

    }

