    private void addScheduleClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addScheduleClassSubmitButtonActionPerformed
        // TODO add your handling code here:
        String courseCode = (String)selectClassComboBox.getSelectedItem();
        StudentEntry student = (StudentEntry)selectStudentComboBox.getSelectedItem();
        int seatsRemaining = ClassQueries.getClassSeats(currentSemester, courseCode);
        seatsRemaining -= ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);
        String status = (seatsRemaining > 0) ? "Scheduled" : "Waitlisted";
        java.sql.Timestamp ts = new java.sql.Timestamp(java.util.Calendar.getInstance().getTime().getTime());
        ScheduleQueries.addScheduleEntry(new ScheduleEntry(currentSemester, courseCode, student.getStudentID(), status.substring(0, 1), ts));
        scheduleClassLabel.setText(student.toString() + " has been " + status + " for the class");
    }//GEN-LAST:event_addScheduleClassSubmitButtonActionPerformed

